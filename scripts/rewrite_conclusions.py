    if q['type'] == 'easter':
        # 从quest story和steps中提取关键信息
        story = q.get('story', '')
        # 从steps中找收尾信息
        last_step = q.get('steps', [])[-1] if q.get('steps') else {}
        on_complete = last_step.get('onComplete', '')
        # 组合成清晰的总结
        if on_complete:
            C[qid] = on_complete
        else:
            C[qid] = story

