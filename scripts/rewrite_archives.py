# Process all character stories
rewritten = 0
for cs in story.get('characterStories', []):
    cid = cs['id']
    if cid in ARCHIVES:
        cs['archiveText'] = ARCHIVES[cid]
        rewritten += 1

# For characters without specific archives, generate a clean template
char_info = {c['id']: c for c in chars}
for cs in story.get('characterStories', []):
    cid = cs['id']
    if cid not in ARCHIVES:
        char = char_info.get(cid, {})
        name = char.get('displayName', cid)
        title = char.get('title', '')
        area = char.get('defaultArea', '')
        style = ', '.join(char.get('dialogueStyle', [])[:3])
        func = char.get('socialFunction', '')
        stats = char.get('stats', {})
        msg_count = stats.get('messageCount', '?')
        active = stats.get('timeHabit', '?')
        mood = stats.get('emotionStyle', '?')

        ARCHIVES[cid] = f"""## 群内身份
{name}，称号{title}。消息数{msg_count}，{active}。

## 说话风格
{style}。

## 群内关系
群聊常驻成员，与其他居民在小镇日常中有交集。

## 小镇日常
常驻{area}区域。"""
        cs['archiveText'] = ARCHIVES[cid]
        rewritten += 1

json.dump(story, open(os.path.join(DATA, 'story_database.json'), 'w'), ensure_ascii=False, indent=2)
print(f"✅ {rewritten} character archives rewritten")

# Export full archive as md
md_lines = ["# 小康在线 · 角色档案\n", "> 群聊小镇居民资料汇编\n"]
for cid in sorted(ARCHIVES.keys()):
    char = char_info.get(cid, {})
    title = char.get('title', '')
    name = char.get('displayName', cid)
    md_lines.append(f"---\n## {name}\n*{title}*\n")
    md_lines.append(ARCHIVES[cid])
    md_lines.append("")

md_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "角色档案.md")
with open(md_path, 'w', encoding='utf-8') as f:
    f.write('\n'.join(md_lines))
print(f"✅ Exported {len(ARCHIVES)} character archives to 角色档案.md")
