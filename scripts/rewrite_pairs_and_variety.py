def main():
    # 1. 找到所有现有的双人对话并替换
    existing_duos = [d for d in dialogues if d['id'].startswith('dialogue_duo_')]
    print(f"现有双人对话: {len(existing_duos)}")

    new_duos = []
    for key, script in PAIR_SCRIPTS.items():
        a_id, b_id = script["a"], script["b"]
        duo_id = f"dialogue_duo_{a_id}_{b_id}"
        # 检查是否已存在对应ID
        new_duos.append({
            "id": duo_id,
            "speaker": a_id,
            "area": script["area"],
            "time": "any",
            "characters": [a_id, b_id],
            "lines": script["lines"],
            "rewards": [f"memory_pair_{a_id}_{b_id}"]
        })

    # 替换 dialogues.json 中的双人对话
    non_duo = [d for d in dialogues if not d['id'].startswith('dialogue_duo_')]
    result = non_duo + new_duos

    # 去重
    seen_ids = set()
    unique_result = []
    for d in result:
        if d['id'] not in seen_ids:
            seen_ids.add(d['id'])
            unique_result.append(d)

    # 写入
    with open(os.path.join(DATA, "dialogues.json"), "w", encoding="utf-8") as f:
        json.dump(unique_result, f, ensure_ascii=False, indent=2)

    print(f"✅ 写入 {len(unique_result)} 条对话 ({len(new_duos)} 条双人对话)")

    # 2. 验证——检查双人对话开场白是否全部唯一
    duo_dialogues = [d for d in unique_result if d['id'].startswith('dialogue_duo_')]
    opener_counts = {}
    for d in duo_dialogues:
        if len(d['lines']) > 1:
            key = d['lines'][1]['text']
            opener_counts[key] = opener_counts.get(key, 0) + 1
    duplicates = {k: v for k, v in opener_counts.items() if v > 1}
    if duplicates:
        print(f"⚠️  仍有 {len(duplicates)} 种开场白被重复使用:")
        for text, count in list(duplicates.items())[:5]:
            print(f"    [{count}x] {text[:60]}")
    else:
        print("✅ 所有双人对话开场白完全唯一！")

if __name__ == "__main__":
    main()
