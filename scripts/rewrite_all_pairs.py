def main():
    non_duo = [d for d in dialogues if not d['id'].startswith('dialogue_duo_')]
    result = non_duo + NEW_PAIRS

    with open(os.path.join(DATA, "dialogues.json"), "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    # 验证
    duos = [d for d in result if d['id'].startswith('dialogue_duo_')]
    from hashlib import md5
    texts = {}
    for d in duos:
        h = md5(json.dumps(d['lines'], ensure_ascii=False).encode()).hexdigest()
        texts.setdefault(h, []).append(d['id'])
    dupes = {k: v for k, v in texts.items() if len(v) > 1}

    print(f"✅ 关系剧情: {len(duos)}对")
    print(f"✅ 全文重复: {len(dupes)}组")

    # 情境分析
    situations = {}
    for d in duos:
        txt = d['lines'][0]['text'] + d['lines'][1]['text']
        if '实验' in txt or '测试' in txt: s = '实验'
        elif '拍' in txt or '照片' in txt or '相机' in txt: s = '拍照'
        elif '查' in txt or '数据' in txt: s = '查数据'
        elif '发现' in txt or '规律' in txt: s = '发现'
        elif '画' in txt or '战报' in txt or '颜料' in txt: s = '创作'
        elif '等' in txt: s = '等待'
        elif '吃' in txt or '菜' in txt or '饭' in txt: s = '食物'
        elif '歌' in txt or '演出' in txt: s = '演出'
        elif '站' in txt or '坐' in txt: s = '陪伴'
        elif '推' in txt or '走' in txt: s = '到来'
        else: s = '其他'
        situations[s] = situations.get(s, 0) + 1
    print(f"情境分布: {dict(sorted(situations.items(), key=lambda x: -x[1]))}")

    print("✅ 重写完成")

if __name__ == "__main__":
    main()
