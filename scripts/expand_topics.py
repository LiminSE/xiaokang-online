import json, os, random

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA = os.path.join(ROOT, "src", "data")

dialogues = json.load(open(os.path.join(DATA, "dialogues.json")))
characters = json.load(open(os.path.join(DATA, "characters.json")))

char_by_id = {c["id"]: c for c in characters}

def ln(s, t): return {"speaker": s, "text": t}
def N(t): return ln("narrator", f"旁白：{t}")
def P(t): return ln("player", t)

# 每个角色的额外闲谈话题（每个角色2-3套）
CASUAL_VARIANTS = {}

# 闲谈话题模板 — 每个角色不同的话题方向
def add_casual(char_id, label, topic_lines):
    if char_id not in CASUAL_VARIANTS:
        CASUAL_VARIANTS[char_id] = []
    CASUAL_VARIANTS[char_id].append({
        "label": label,
        "lines": topic_lines
    })

# 长聊话题模板
DEEP_VARIANTS = {}
def add_deep(char_id, label, topic_lines):
    if char_id not in DEEP_VARIANTS:
        DEEP_VARIANTS[char_id] = []
    DEEP_VARIANTS[char_id].append({
        "label": label,
        "lines": topic_lines
    })

